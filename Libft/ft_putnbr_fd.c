#include "libft.h"

void	ft_putnbr_fd(int n, int fd)
{
	int	mod;

	if (n == -2147483648)
	{
		write(fd, "-2147483648", 11);
		return ;
	}
	if (n < 0)
	{
		n = n * -1;
		write(fd, "-", 1);
	}
	if (n > 9)
	{
		mod = n % 10 + '0';
		ft_putnbr_fd(n / 10, fd);
	}
	else
		mod = n + '0';
	write(fd, &mod, 1);
}
